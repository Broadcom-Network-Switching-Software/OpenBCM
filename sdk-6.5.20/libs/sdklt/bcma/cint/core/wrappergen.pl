sub plist
  {
    my $i = shift;
    my @pre = @_;
    my @params = map { "$_$i" } @pre;
    return join ",", @params;
  }

sub pplist
  {
    my $num = shift;
    my @pre = @_;

    my $string;

    for(my $x = 0; $x < $num; $x++) {
      $string = $string . "," . plist($x, @pre);
    }
    return $string;
  }

sub ctrim
  {
    my $string = shift;
    $string =~ s/^,//;
    return $string;
  }

sub macro
  {
    my $define = shift;
    my $name = shift;
    my $returns = shift;
    my $fname = shift;
    my $pcount = shift;
    my @pre = @_;

    printf "%s${name}_%sP%d(%s%s%s)  %s",
      $define ? "#define " : "",
      defined($returns) ? "R" : "V",
        $pcount,
          $returns, $fname, pplist($pcount, @pre), $define ? "\\\n" : "";
  }


sub fwrapper_function
  {
    my ($returns, $count) = @_;
    my $rs;

    if($returns) {
      $rs = "rt,";
    }
    macro(1, "CINT_FWRAPPER_FUNCTION", $rs, "fname", $count, qw(pt p));
    print "static int __cint_fwrapper__##fname (cint_fparams_t* fparams) \\\n";
    print "{ \\\n";
    if($count) {
      printf "  CINT_FARGS%d(%s);  \\\n",
        $count, ctrim(pplist($count, qw(pt p)));
    }
    print "\\\n";
    if($returns) {
      print "  CINT_FRET(rt) = ";
    }
    else {
      print "  ";
    }
    my $list = ctrim(pplist($count, qw(p)));
    printf "fname (%s); \\\n", $list;
    print "\\\n";
    if($count) {
      print "  CINT_FWRAPPER_END;\\\n";
    }
    print "  return 0; \\\n";
    print "}\n\n";
  }

sub f_params
  {
    my ($name, $returns, $count) = @_;
