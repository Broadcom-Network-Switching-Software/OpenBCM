    if($returns) {
      $rs = "rb,rp,ra,";
    }
    macro(1, "CINT_${name}_PARAMS", $rs, "fname", $count, qw(pb p pp pa));
    print "static cint_parameter_desc_t __cint_parameters__##fname [] = \\\n";
    print "{ \\\n";
    if($returns) {
      print "  { #rb, \"r\", rp, ra }, \\\n";
    }
    else {
      print "  { \"void\", \"r\", 0, 0 }, \\\n";
    }
    for(my $i = 0; $i < $count; $i++) {
      my $list = ctrim(plist($i, qw(#pb #p pp pa)));
      print "  { $list }, \\\n";
    }
    print "  CINT_ENTRY_LAST \\\n";
    print "}\n\n";
  }

