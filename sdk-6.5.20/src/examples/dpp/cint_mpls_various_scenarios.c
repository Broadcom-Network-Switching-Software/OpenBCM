    if (rv != BCM_E_NONE) {
        printf("Error, in mpls_port__mp_create_ingress_egress_mpls_port\n");
        return rv;
    }

    cint_mpls_various_scenarios_info.mpls_port_id1 = mpls_port_properties[0].mpls_port_id;

