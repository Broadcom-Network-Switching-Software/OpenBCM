shr_error_e
_dbal_init_tables_hard_logic_pp_arr_hl_pp_arr_fhei_arr_prefix_init(
int unit,
table_db_struct_t * cur_table_param,
dbal_logical_table_t * table_info)
{
    dbal_logical_table_t * table_entry = table_info + DBAL_TABLE_FHEI_ARR_PREFIX;
    int is_valid = TRUE;
    uint8 is_skip_table_init = FALSE;
    char * current_image = NULL;
    dbal_maturity_level_e maturity_level;
    SHR_FUNC_INIT_VARS(unit);
    dbal_init_table_db_struct_clear(cur_table_param);
    maturity_level = DBAL_MATURITY_HIGH;
    SHR_IF_ERR_EXIT(dbal_db_init_table_general_info_and_image_handle(unit, table_entry, DBAL_TABLE_FHEI_ARR_PREFIX, is_valid, maturity_level, "FHEI_ARR_PREFIX", DBAL_ACCESS_METHOD_HARD_LOGIC, current_image, &is_skip_table_init));
    if (is_skip_table_init)
    {
        SHR_EXIT();
    }
    table_entry->table_type = DBAL_TABLE_TYPE_DIRECT;
    table_entry->nof_labels = 0;
    DBAL_DB_INIT_TABLE_LABELS_ALLOC(table_entry)
    {
        int field_index = 0;
        dbal_table_field_info_t * dbal_db_field;
        table_db_field_params_struct_t db_field;
        DBAL_DB_INIT_TABLE_INTERFACE_KEY_FIELDS_ALLOC(table_entry, 1);
        {
            dbal_db_field = &table_entry->keys_info[field_index];
            SHR_IF_ERR_EXIT(dbal_db_init_table_field_set_mandatory_values(unit, DBAL_FIELD_ETPS_FHEI_FIELD, DBAL_FIELD_TYPE_DEF_ETPS_FHEI_FIELD, dbal_db_field));
            dbal_db_init_table_field_params_init(&db_field);
            SHR_IF_ERR_EXIT(dbal_db_init_table_field_set_optional_or_default_values(unit, table_entry, dbal_db_field, &db_field, TRUE));
            field_index += dbal_db_field->nof_instances;
        }
        table_entry->nof_key_fields = field_index;
        dbal_db_init_table_calculate_key_size(table_entry);
    }
    {
        int field_index = 0;
        int result_type_counter = 0;
        dbal_table_field_info_t * dbal_db_field;
        table_db_field_params_struct_t db_field;
        DBAL_DB_INIT_TABLE_NOF_RESULT_TYPES_SET(table_entry, 1);
        SHR_IF_ERR_EXIT(dbal_db_init_table_db_interface_results_alloc(unit, table_entry));
        DBAL_DB_INIT_TABLE_NOF_RESULT_FIELDS_SET(table_entry, (table_entry->multi_res_info[result_type_counter]), 1);
        DBAL_DB_INIT_TABLE_INTERFACE_RESULT_FIELDS_ALLOC(table_entry, table_entry->multi_res_info[result_type_counter]);
        {
            dbal_db_field = &table_entry->multi_res_info[result_type_counter].results_info[field_index];
            SHR_IF_ERR_EXIT(dbal_db_init_table_field_set_mandatory_values(unit, DBAL_FIELD_PREFIX_VALUE, DBAL_FIELD_TYPE_DEF_UINT, dbal_db_field));
            dbal_db_init_table_field_params_init(&db_field);
            db_field.size = 8;
            SHR_IF_ERR_EXIT(dbal_db_init_table_field_set_optional_or_default_values(unit, table_entry, dbal_db_field, &db_field, FALSE));
            field_index += dbal_db_field->nof_instances;
        }
    }
    dbal_db_init_table_db_struct_access_clear(cur_table_param, table_entry);
    table_entry->core_mode = DBAL_CORE_MODE_SBC;
    dbal_db_init_table_add_result_type_physical_values_by_result_type_index(table_entry);
    {
        int map_idx = 0;
        int access_counter;
        map_idx = 0;
        access_counter = 0;
        sal_strncpy(cur_table_param->hl_access[map_idx].mapping_result_name, EMPTY, sizeof(cur_table_param->hl_access[map_idx].mapping_result_name));
        {
            table_db_access_params_struct_t * access_params;
            {
                int condition_index = 0;
                access_params = &cur_table_param->hl_access[map_idx].access[access_counter];
                dbal_db_init_table_db_struct_hl_access_params_clear(access_params);
                access_counter++;
                access_params->access_field_id = DBAL_FIELD_PREFIX_VALUE;
                access_params->access_type = DBAL_HL_ACCESS_MEMORY;
                SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_condition(unit, &access_params->access_condition[condition_index], DBAL_CONDITION_EQUAL_TO, DBAL_FIELD_ETPS_FHEI_FIELD, 0, (uint32) DBAL_ENUM_FVAL_ETPS_FHEI_FIELD_TTL_IDX));
                condition_index++;
                access_params->nof_conditions = condition_index;
                {
                    SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_register_memory(unit, access_params, DBAL_HL_ACCESS_HW_ENTITY_INVALID, PREFIXf));
                    DBAL_DB_INIT_ACCESS_PARAMS_GROUP_SET(access_params, DBAL_HW_ENTITY_GROUP_ETPP_ARR_PREFIX_GROUP);
                    {
                        access_params->entry_offset.formula_int = 2;
                    }
                }
            }
            {
                int condition_index = 0;
                access_params = &cur_table_param->hl_access[map_idx].access[access_counter];
                dbal_db_init_table_db_struct_hl_access_params_clear(access_params);
                access_counter++;
                access_params->access_field_id = DBAL_FIELD_PREFIX_VALUE;
                access_params->access_type = DBAL_HL_ACCESS_MEMORY;
                SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_condition(unit, &access_params->access_condition[condition_index], DBAL_CONDITION_EQUAL_TO, DBAL_FIELD_ETPS_FHEI_FIELD, 0, (uint32) DBAL_ENUM_FVAL_ETPS_FHEI_FIELD_QOS_MODEL));
                condition_index++;
                access_params->nof_conditions = condition_index;
                {
                    SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_register_memory(unit, access_params, DBAL_HL_ACCESS_HW_ENTITY_INVALID, PREFIXf));
                    DBAL_DB_INIT_ACCESS_PARAMS_GROUP_SET(access_params, DBAL_HW_ENTITY_GROUP_ETPP_ARR_PREFIX_GROUP);
                    {
                        access_params->entry_offset.formula_int = 6;
                    }
                }
            }
            {
                int condition_index = 0;
                access_params = &cur_table_param->hl_access[map_idx].access[access_counter];
                dbal_db_init_table_db_struct_hl_access_params_clear(access_params);
                access_counter++;
                access_params->access_field_id = DBAL_FIELD_PREFIX_VALUE;
                access_params->access_type = DBAL_HL_ACCESS_MEMORY;
                SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_condition(unit, &access_params->access_condition[condition_index], DBAL_CONDITION_EQUAL_TO, DBAL_FIELD_ETPS_FHEI_FIELD, 0, (uint32) DBAL_ENUM_FVAL_ETPS_FHEI_FIELD_QOS_IDX));
                condition_index++;
                access_params->nof_conditions = condition_index;
                {
                    SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_register_memory(unit, access_params, DBAL_HL_ACCESS_HW_ENTITY_INVALID, PREFIXf));
                    DBAL_DB_INIT_ACCESS_PARAMS_GROUP_SET(access_params, DBAL_HW_ENTITY_GROUP_ETPP_ARR_PREFIX_GROUP);
                    {
                        access_params->entry_offset.formula_int = 7;
                    }
                }
            }
            {
                int condition_index = 0;
                access_params = &cur_table_param->hl_access[map_idx].access[access_counter];
                dbal_db_init_table_db_struct_hl_access_params_clear(access_params);
                access_counter++;
                access_params->access_field_id = DBAL_FIELD_PREFIX_VALUE;
                access_params->access_type = DBAL_HL_ACCESS_MEMORY;
                SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_condition(unit, &access_params->access_condition[condition_index], DBAL_CONDITION_EQUAL_TO, DBAL_FIELD_ETPS_FHEI_FIELD, 0, (uint32) DBAL_ENUM_FVAL_ETPS_FHEI_FIELD_TTL_MODEL));
                condition_index++;
                access_params->nof_conditions = condition_index;
                {
                    SHR_IF_ERR_EXIT(dbal_db_init_table_set_access_register_memory(unit, access_params, DBAL_HL_ACCESS_HW_ENTITY_INVALID, PREFIXf));
                    DBAL_DB_INIT_ACCESS_PARAMS_GROUP_SET(access_params, DBAL_HW_ENTITY_GROUP_ETPP_ARR_PREFIX_GROUP);
                    {
                        access_params->entry_offset.formula_int = 12;
                    }
                }
            }
        }
        cur_table_param->hl_access[map_idx].nof_access = access_counter;
    }
    SHR_IF_ERR_EXIT(dbal_db_init_table_add(unit, cur_table_param, DBAL_TABLE_FHEI_ARR_PREFIX, table_info));
exit:
    SHR_FUNC_EXIT;
}

